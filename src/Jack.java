public class Jack {

	private int age;
	private String hairColor;

public Jack(int age, String hairColor) {
	this.age = age;
	this.hairColor = hairColor;
}

public int getAge() {
	return age;
}

public void setAge() {
	this.age = age;
}

public String getHairColor() {
	return hairColor;
}

public void setHairColor() {
	this.hairColor = hairColor;
	
}

}